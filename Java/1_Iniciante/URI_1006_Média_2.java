/*
|>Leia 3 valores, no caso, variáveis A, B e C, que são as três notas de um aluno. A seguir, calcule a média do aluno, sabendo que a 
nota A tem peso 2, a nota B tem peso 3 e a nota C tem peso 5. Considere que cada nota pode ir de 0 até 10.0, sempre com uma casa de-
cimal.

   Condições:
   
   1)-O arquivo de entrada contém 3 valores com uma casa decimal, de dupla precisão (double);
   
   2)- Imprima a mensagem "MEDIA" e a média do aluno conforme exemplo abaixo, com 1 dígito após o ponto decimal e com um espaço em bran-
   co antes e depois da igualdade.
   
   3)-Não esqueça de imprimir o fim de linha após o produto, caso contrário seu programa apresentará a mensagem: “Presentation Error”.

          | Exemplos de Entrada | Exemplos de Saída |
          |         5,0                             |
          |         6,0                             |
          |         7,0         | MEDIA = 6,3       |
          |-----------------------------------------|                
          |         5,0                             |
          |        10,0                             |
          |        10,0         | MEDIA = 9,0       |
          |-----------------------------------------|
          |        10.0                             |
          |        10,0                             |
          |         5.0         | MEDIA = 7,5       |
          |_________________________________________|


______________________________________________________________________________________________________________________________________
*/

package URI_1_Iniciante;

import java.util.Scanner;

public class URI_1006_Média_2 {

	public static void main(String[] args) {
		
		Scanner entrada = new Scanner(System.in); //declaração de um Scanner, para obter entrada do console
		
		double A = entrada.nextDouble(),
			   B = entrada.nextDouble(),
			   C = entrada.nextDouble(),
		   MEDIA = ((A * 2.0) + (B * 3.0) + (C * 5.0)) / 10.0;
		
		System.out.printf("MEDIA = %.1f", MEDIA);
				
        entrada.close();
	}

}
